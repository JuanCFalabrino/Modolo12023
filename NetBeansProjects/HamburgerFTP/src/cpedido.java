
public class cpedido {
    
    private String cpnom;
    private String cptele;
    private String cpbebe;
    private String cppf;
    private String cphb;
    
    public cpedido(String NOM,String TEL,String BB,String PF,String HB){
    
        this.cpnom = NOM;
        this.cptele=TEL;
        this.cpbebe=BB;
        this.cppf=PF;
        this.cphb=HB;
            
    }



 

    public String getCpnom() {
        return cpnom;
    }

    public String getCptele() {
        return cptele;
    }

    public String getCpbebe() {
        return cpbebe;
    }

    public String getCppf() {
        return cppf;
    }

    public String getCphb() {
        return cphb;
    }
    
    
    
    
    
    
}
